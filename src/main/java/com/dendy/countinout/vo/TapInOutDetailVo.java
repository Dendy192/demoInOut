public class TapInOutDetailVo {
    private String gateName;
    private String tapIn;
    private String tapOut;
    private String tapInOut;

    public String getGateName() {
        return gateName;
    }

    public void setGateName(String gateName) {
        this.gateName = gateName;
    }

    public String getTapIn() {
        return tapIn;
    }

    public void setTapIn(String tapIn) {
        this.tapIn = tapIn;
    }

    public String getTapOut() {
        return tapOut;
    }

    public void setTapOut(String tapOut) {
        this.tapOut = tapOut;
    }

    public String getTapInOut() {
        return tapInOut;
    }

    public void setTapInOut(String tapInOut) {
        this.tapInOut = tapInOut;
    }
}
